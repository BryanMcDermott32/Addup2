package AddUpPack;

public class Calculator {

    public int addUp(int a, int b){
        return a+b;
    }

    public int subtract(int a, int b) {
        return a-b;
    }

    public int multiply(int a, int b) {
        return a*b;
    }

    public int divide(int a, int b) {
        return a/b;
    }

    public int addThree(int a, int b, int c) {
        return a+b+c;
    }
}
